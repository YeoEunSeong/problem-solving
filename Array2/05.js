function solution(board) {
  let answer = 0;
  const dx = [1, 0, -1, 1];
  const dy = [0, 1, -1, 1];

  let nboard = Array.from(Array(21), () => Array(21).fill(0));

  for (let i = 1; i < 20; i++) {
    for (let j = 1; j < 20; j++) {
      nboard[i][j] = board[i - 1][j - 1];
    }
  }

  outer: for (let row = 1; row < 20; row++) {
    for (let col = 1; col < 20; col++) {
      if (nboard[row][col] === 0) continue;

      for (let direction = 0; direction < 4; direction++) {
        let cnt = 1;
        let x = row;
        let y = col;

        while (0 <= x && x < 20 && 0 <= x && y < 20) {
          x += dx[direction];
          y += dy[direction];

          if (nboard[x][y] !== nboard[row][col]) break;
          cnt++;
        }

        if (cnt === 5) {
          answer = nboard[row][col];
          console.log(row, col);
          break outer;
        }
      }
    }
  }

  return answer;
}

